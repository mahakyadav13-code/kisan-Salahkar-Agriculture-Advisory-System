// ── Language manager ────────────────────────────────────────────
const LangManager = {
    current: localStorage.getItem("ks_lang") || "en",

    init() {
        this.apply();
        document.querySelectorAll(".lang-toggle").forEach(btn => {
            btn.addEventListener("click", () => this.toggle());
        });
    },

    toggle() {
        this.current = this.current === "en" ? "hi" : "en";
        localStorage.setItem("ks_lang", this.current);
        this.apply();
    },

    t(key) {
        return LANG[this.current][key] ?? LANG.en[key] ?? key;
    },

    apply() {
        const t = (k) => this.t(k);
        document.documentElement.lang = this.current;

        document.querySelectorAll(".lang-toggle-text").forEach(el => {
            el.textContent = t("langLabel");
        });

        document.querySelectorAll("[data-i18n]").forEach(el => {
            const key = el.dataset.i18n;
            const val = t(key);
            if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
                el.placeholder = val;
            } else {
                el.textContent = val;
            }
        });

        document.querySelectorAll("[data-i18n-html]").forEach(el => {
            const key = el.dataset.i18nHtml;
            el.innerHTML = t(key);
        });

        ["govtItems", "pestItems", "modernItems", "soilItems", "irrigationItems", "marketItems"].forEach(key => {
            const container = document.getElementById(key);
            if (!container) return;
            const items = LANG[this.current][key] || LANG.en[key];
            container.innerHTML = items.map(item => `<li>${item}</li>`).join("");
        });
    }
};
